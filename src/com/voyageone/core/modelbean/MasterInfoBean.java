package com.voyageone.core.modelbean;

/**
 * @author jacky
 *
 */
public class MasterInfoBean {

	/**
	 * 类型
	 */
	private int type;
	
	/**
	 * 实际值
	 */
	private String id;
	
	/**
	 * 显示名
	 */
	private String name;

	/**
	 * @return the type
	 */
	public int getType() {
		return type;
	}

	/**
	 * @param type the type to set
	 */
	public void setType(int type) {
		this.type = type;
	}

	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	
}
